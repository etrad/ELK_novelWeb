package com.sif.pojo;

public class TopDetail {

    private String author;

    private Double clickCount;

    private long sumBooks;

    private String pic;

    public String getAuthor() {
        return author;
    }

    public void setAuthor(String author) {
        this.author = author;
    }

    public Double getClickCount() {
        return clickCount;
    }

    public void setClickCount(Double clickCount) {
        this.clickCount = clickCount;
    }

    public long getSumBooks() {
        return sumBooks;
    }

    public void setSumBooks(long sumBooks) {
        this.sumBooks = sumBooks;
    }

    public String getPic() {
        return pic;
    }

    public void setPic(String pic) {
        this.pic = pic;
    }
}
